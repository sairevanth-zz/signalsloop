/**
 * PDF Export API
 * Generates PDF from stakeholder query response
 */

import { NextRequest, NextResponse } from 'next/server';

export const runtime = 'nodejs';

export async function POST(request: NextRequest) {
  try {
    const body = await request.json();
    const { queryText, userRole, components, projectId } = body;

    if (!queryText || !userRole || !components) {
      return NextResponse.json(
        { error: 'Missing required fields: queryText, userRole, components' },
        { status: 400 }
      );
    }

    // Generate a simple HTML representation
    // In a real implementation, you'd use a library like puppeteer or playwright
    // to render the actual components and convert to PDF
    const html = generateHTMLReport(queryText, userRole, components, projectId);

    return NextResponse.json({
      success: true,
      html,
      message: 'PDF data generated successfully'
    });
  } catch (error) {
    console.error('[Export PDF API] Error:', error);
    return NextResponse.json(
      {
        error: 'Failed to generate PDF',
        details: error instanceof Error ? error.message : 'Unknown error'
      },
      { status: 500 }
    );
  }
}

