/**
 * Render individual component as HTML
 */
function renderComponent(component: any, index: number): string {
  const { type, props } = component;

  switch (type) {
    case 'SummaryText':
      return `
        <div class="component">
          <div class="content">
            ${props.content}
          </div>
        </div>
      `;

    case 'MetricCard':
      return `
        <div class="component">
          <div class="component-title">${props.title}</div>
          <div class="metric">${props.value}</div>
          <div class="content">
            ${props.delta ? `<div>${props.delta}</div>` : ''}
            ${props.description ? `<div>${props.description}</div>` : ''}
          </div>
        </div>
      `;

    case 'SentimentChart':
      return `
        <div class="component">
          <div class="component-title">${props.title || 'Sentiment Trend'}</div>
          <div class="content">
            <p>Sentiment chart visualization (${props.timeRange})</p>
            <p><em>Chart data is best viewed in the interactive dashboard.</em></p>
          </div>
        </div>
      `;

    case 'ThemeCloud':
      return `
        <div class="component">
          <div class="component-title">${props.title || 'Top Themes'}</div>
          <div class="content">
            ${props.themes?.map((theme: any) =>
              `<span class="theme-item">${theme.name} (${theme.count})</span>`
            ).join('') || ''}
          </div>
        </div>
      `;

    case 'FeedbackList':
      return `
        <div class="component">
          <div class="component-title">${props.title || 'Feedback'}</div>
          <div class="content">
            ${props.items?.map((item: any) => `
              <div class="feedback-item">
                <strong>${item.title}</strong>
                ${item.content ? `<div>${item.content}</div>` : ''}
                <div style="font-size: 12px; color: #9ca3af; margin-top: 4px;">
                  Sentiment: ${item.sentiment?.toFixed(2) || 'N/A'} | Source: ${item.source || 'Unknown'}
                </div>
              </div>
            `).join('') || '<p>No feedback items</p>'}
          </div>
        </div>
      `;

    case 'ActionCard':
      return `
        <div class="component" style="border-left: 4px solid ${props.severity === 'high' ? '#ef4444' : props.severity === 'medium' ? '#f59e0b' : '#3b82f6'}">
          <div class="component-title">âš¡ ${props.title}</div>
          <div class="content">
            <div><strong>Action Required:</strong> ${props.action}</div>
            ${props.description ? `<div>${props.description}</div>` : ''}
            <div style="margin-top: 8px; font-size: 14px;">
              <strong>Severity:</strong> ${props.severity?.toUpperCase() || 'UNKNOWN'}
            </div>
          </div>
        </div>
      `;

    case 'CompetitorCompare':
      return `
        <div class="component">
          <div class="component-title">${props.title || 'Competitive Comparison'}</div>
          <div class="content">
            <p><strong>Competitors:</strong> ${props.competitors?.join(', ') || 'None'}</p>
            ${props.metrics?.map((metric: any) => `
              <div style="margin: 8px 0;">
                <strong>${metric.name}:</strong> ${metric.value}
              </div>
            `).join('') || ''}
          </div>
        </div>
      `;

    case 'TimelineEvents':
      return `
        <div class="component">
          <div class="component-title">${props.title || 'Timeline Events'}</div>
          <div class="content">
            ${props.events?.map((event: any) => `
              <div class="feedback-item">
                <strong>${event.title}</strong>
                ${event.description ? `<div>${event.description}</div>` : ''}
                <div style="font-size: 12px; color: #9ca3af; margin-top: 4px;">
                  ${new Date(event.date).toLocaleDateString()} | Type: ${event.type}
                </div>
              </div>
            `).join('') || '<p>No events</p>'}
          </div>
        </div>
      `;

    default:
      return `
        <div class="component">
          <div class="content">Unknown component type: ${type}</div>
        </div>
      `;
  }
}
